    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        ProjectLeader leader = new ProjectLeader();
        leader.setProjectId(Integer.parseInt(txtProjectId.getText()));

        String[] emps = new String[0];

        List<String> empl = new ArrayList<String>();

        for (int i : jList1.getSelectedIndices()) {
            empl.add(model.get(i));

        }

        leader.setEmployees(empl.toArray(emps));

        leader.setAddress(emp.getAddress());
        leader.setAllowances(emp.getAllowances());
        leader.setDOB(emp.getDOB());
        leader.setEmail(emp.getEmail());
        leader.setGender(emp.getGender());
        leader.setId(emp.getId());
        leader.setNIC(emp.getNIC());
        leader.setName(emp.getName());
        leader.setSalary(emp.getSalary());
        leader.setSkills(emp.getSkills());
        leader.setTP(emp.getTP());

        try {
            DataHandler.getInstance().deletePersonByNIC(leader.getNIC());
        } catch (IndexOutOfBoundsException e) {
        }

        DataHandler.getInstance().add(leader);
        JOptionPane.showInternalMessageDialog(this, "Entry added/updated!");
        this.dispose();

    }//GEN-LAST:event_btnSaveActionPerformed
