    public AddTeamLeader(PermanentEmp emp) {
        this.emp = emp;
        initComponents();


        for (Person P : DataHandler.getInstance().getAllPeople()) {
            if (P instanceof Employee) {
                model.addElement(((Employee) P).getId());
            }
        }

        jList1.setModel(model);

        if (emp instanceof ProjectLeader) {
            this.showTeamLeader((ProjectLeader) emp);
        }

        pack();
    }

    public void showTeamLeader(ProjectLeader e) {


        txtProjectId.setText(Integer.toString(e.getProjectId()));

        for (String s : e.getEmployees()) {
            System.out.println(s);
            for (int i = 0; i < model.getSize(); i++) {
                if (model.get(i).equalsIgnoreCase(s)) {
                    jList1.setSelectedValue(model.get(i), true);
                }
            }
        }



    }

